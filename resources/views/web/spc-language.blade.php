					<h4 class="spc-heading">5. PHARMACOLOGICAL PROPERTIES  </h4>
					<strong>5.1 Pharmacodynamic properties</strong>
					<p>Pharmacotherapeutic group: plasma substitutes and plasma protein fractions, ATC code: B05AA01. </p>
					<p>Human albumin accounts quantitatively for more than half of the total protein in the plasma and represents about 10 % of the protein synthesis activity of the liver.  </p>
					<p>Physico-chemical data: Human Albumin 200 g/l Baxter has a corresponding hyperoncotic effect.</p>
					<p>The most important physiological functions of albumin result from its contribution to the oncotic pressure of the blood and its transport function. Albumin stabilizes circulating blood volume and is a carrier of hormones, enzymes, medicinal products and toxins.</p>
					<strong>5.2 Pharmacokinetic properties </strong>
					<p>Under normal conditions the total exchangeable albumin pool is 4 – 5 g/kg bodyweight, of which 40 to 45 % is present intravascularly and 55 to 60 % in the extravascular space. Increased capillary permeability will alter albumin kinetics and abnormal distribution may occur in conditions such as severe burns or septic shock. </p>
					<p>Under normal conditions, the average half life of albumin is about 19 days. The balance between synthesis and breakdown is normally achieved by feedback regulation. Elimination is predominantly intracellular and due to lysosome proteases. </p>
					<p>In healthy subjects, less than 10 % of infused albumin leaves the intravascular compartment during the first two hours following infusion. There is considerable individual variation in the effect on plasma volume. In some patients the plasma volume can remain increased for some hours. However, in critically ill patients, albumin can leak out of the vascular space in substantial amounts at an unpredictable rate. </p>
					<strong>5.3 Preclinical safety data </strong>
					<p>Human albumin is a normal constituent of human plasma and acts like physiological albumin. </p>
					<p>In animals, single dose toxicity testing is of little relevance and does not permit the evaluation of toxic or lethal doses or of a dose-effect relationship. Repeated dose toxicity testing is impracticable due to the development of antibodies to heterologous protein in animal models.  </p>
					<p>To date, human albumin has not been reported to be associated with embryofoetal toxicity, oncogenic or mutagenic potential. </p>
					<p>No signs of acute toxicity have been described in animal models.</p>
