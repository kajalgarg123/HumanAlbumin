							<p>Une hypervolémie peut se produire lorsque la posologie et le débit de perfusion sont trop élevés. Il faut interrompre immédiatement la perfusion dès l’apparition des premiers signes cliniques de surcharge cardiovasculaire (céphalées, dyspnée, turgescence des veines jugulaires) ou d’une augmentation de la pression artérielle, de la pression veineuse centrale ou d’un oedème pulmonaire, et contrôler soigneusement les paramètres hémodynamiques du patient.</p>
