							<h4 class="spc-heading">5. PROPRIETES PHARMACOLOGIQUES </h4>
							<strong>5.1. Propriétés pharmacodynamiques </strong>
							<p>Groupe pharmacothérapeutique : substituts du plasma et fractions protéiques plasmatiques, Code ATC : B05AA01. L’albumine humaine représente quantitativement plus de la moitié des protéines totales du plasma et environ 10 % de l’activité de la synthèse des protéines du foie. </p>
							<p>Données physico-chimiques : l’ALBUMINE HUMAINE 200 g/l BAXTER, a un effet hyperoncotique. </p>
							<p>Les fonctions physiologiques les plus importantes de l’albumine tiennent à sa contribution à la pression oncotique du sang et à sa fonction de transporteur. L’albumine stabilise le volume de sang circulant et assure un rôle de transporteur d’hormones, d’enzymes, de médicaments et de toxines.</p>
							<strong>5.2. Propriétés pharmacocinétiques </strong>
							<p>Dans des conditions normales, la teneur physiologique en albumine est de 4 à 5 g/kg de poids corporel, dont 40 à 45 % se trouvent dans l’espace intravasculaire et 55 à 60 % dans l’espace extravasculaire. Une augmentation de la perméabilité des capillaires altère la cinétique de l’albumine et une distribution anormale peut survenir dans certaines conditions telles que brûlures étendues ou choc septique. </p>
							<p>Dans des conditions normales, la demi-vie moyenne de l’albumine est d’environ 19 jours. Un mécanisme de rétrocontrôle permet de maintenir l’équilibre entre sa synthèse et sa dégradation. L’élimination est essentiellement intracellulaire et est assurée par les protéases lysosomiales. </p>
							<p>Chez les sujets sains, moins de 10% de l’albumine perfusée quitte le compartiment intravasculaire durant les deux premières heures qui suivent la perfusion. Les effets sur le volume plasmatique varient considérablement d’un individu à l’autre. Chez certains patients, l’augmentation du volume plasmatique peut perdurer pendant plusieurs heures. Cependant, chez les patients en état critique, on peut observer une fuite intravasculaire de l’albumine en quantités importantes à un taux non prévisible. </p>
							<strong>5.3. Données de sécurité préclinique</strong>
							<p>L’albumine humaine est un constituant normal du plasma humain et agit comme l’albumine physiologique. </p>
							<p>Chez l’animal, les études de toxicité par administration unique sont peu pertinentes et ne permettent ni d’estimer les doses toxiques ou létales ni d’établir une relation dose-effet. Les études de toxicité par administration réitérée ne peuvent être pratiquées en raison du développement d’anticorps contre les protéines hétérologues dans les modèles animaux. A ce jour, aucun cas de toxicité embryo-foetale, de pouvoir oncogène ou mutagène n’a été associé à l’albumine humaine. Aucun signe de toxicité aiguë n’a été décrit dans les modèles animaux.</p>
